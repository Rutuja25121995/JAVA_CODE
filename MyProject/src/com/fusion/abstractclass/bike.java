package com.fusion.abstractclass;

public class bike extends Vehicle {

	@Override
	public void avgweight() {
		System.out.println("average weight of bike--110kg");
		
	}

	@Override
	public void avglength() {
		System.out.println("average lenght of bike--2m");
		
	}

	@Override
	public void avgSpeed() {
		System.out.println("average speed of bike--90km/hr");
		
	}

}
