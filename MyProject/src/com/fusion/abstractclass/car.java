package com.fusion.abstractclass;

public class car extends Vehicle {

	@Override
	public void avgweight() {
		System.out.println("average weight of car--1200kg");
		
	}

	@Override
	public void avglength() {
		System.out.println("average length of car --5m");
		
	}

	@Override
	public void avgSpeed() {
		System.out.println("max speed of car--200km/hr");
		
	}

	

}
